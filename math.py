#ADDITION
x = 5
y = 7
print(x + y)

#SUBTRACTION
print(x - y)

#MULTIPLICATION
print(x * y)

#DIVISION
print(x / y) # DIVISION WITH SINGLE SLASH ALWAYS FIVES A FLOATING POINT NUMBER

#FLOOR DIVISION
print(x // y) # RESULT IS AN INTERGER

#EXPONENTS
print(2 ** 3)

#REMAINDER
print(10 % 3)
print(9 % 3)

#ORDER OF OPERATIONS
op = 10 + ((10 * 2) / 3) - 3
print(op)